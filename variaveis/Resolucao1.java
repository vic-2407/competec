/*
1. Crie uma variável que informa a primeira letra do seu nome;
2. Crie uma variável que informa a sua idade;
3. Crie uma variável informando se você tomou café da manhã;
*/

public class Resolucao1 {
    public static void main(String[] args) {        
        // valores char devem estar entra aspas simples ''
        char primeiraLetra = 'M';

        // valores inteiros são representados apenas por números
        int idade = 24;

        // booleanos podem receber true (verdadeiro) ou false (falso)
        boolean tomouCafe = true;

        // ainda temos os tipos numérico com ponto flutuante
        // valores float devem terminar com a letra f (por isso o uso de double é mais recomendado)
        float precoBanana = 3.5f;
        double pesoEmKg = 65.8;

    }
}
